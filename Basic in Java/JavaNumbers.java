public class JavaNumbers {
    public static void main(String[] args) {
        byte myNum =100; // -128 to  -127
        System.out.println(myNum);

        short MyNum =7000; // -32768 to 32767
        System.out.println(MyNum);

        int MYNum =1000000; // -2,147,487,648 to 2,147,483,647 
        System.out.println(MYNum);

        long MYNUM =1500000000000000000L; //-9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
        System.out.println(MYNUM);

        float myNUM =19.99f; // 
        System.out.println(myNUM);

        double mYnUm =18.87d;
        System.out.println(mYnUm);
    }
}
